    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
        // TODO add your handling code here:
        // Remove the current panel from the container
        userProcessContainer.remove(this);

        // Refresh and navigate to the previous panel
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);

        // Refresh the container to ensure proper layout update
        userProcessContainer.revalidate();
        userProcessContainer.repaint();
    }//GEN-LAST:event_btnBackActionPerformed

    private void btnRessolveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRessolveActionPerformed
        selectedIssue.setStatus("Resolved");

    // Update line status to "Operational"
    Line line = commuterSystem.getLineDirectory().findLineByName(selectedIssue.getLineName());
    if (line != null) {
        line.setStatus("Operational");
    } else {
        JOptionPane.showMessageDialog(this, "Line not found for this issue.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Show confirmation message
    JOptionPane.showMessageDialog(this, "Issue marked as Resolved. Line status updated to Operational.", "Success", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnRessolveActionPerformed

    private void btnInprogressActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInprogressActionPerformed
        selectedIssue.setStatus("In-Progress");

    // Update line status to "Delayed"
    Line line = commuterSystem.getLineDirectory().findLineByName(selectedIssue.getLineName());
    if (line != null) {
        line.setStatus("Delayed");
    } else {
        JOptionPane.showMessageDialog(this, "Line not found for this issue.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Show confirmation message
    JOptionPane.showMessageDialog(this, "Issue marked as In-Progress. Line status updated to Delayed.", "Success", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnInprogressActionPerformed

