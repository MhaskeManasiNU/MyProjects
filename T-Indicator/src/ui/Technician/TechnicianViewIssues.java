    private void populateIssueDetails() {
    if (selectedIssue != null) {
        jTextField1.setText(selectedIssue.getIssueID());
        jTextField2.setText(selectedIssue.getLineName());
        jTextField3.setText(selectedIssue.getDescription());
        jTextField4.setText(selectedIssue.getStatus());
        jTextField5.setText(selectedIssue.getRaisedBy());
    } else {
        JOptionPane.showMessageDialog(this, "No issue data available.", "Error", JOptionPane.ERROR_MESSAGE);
    }
}

    private void setFieldsNonEditable() {
        if (selectedIssue != null) {
        jTextField1.setText(selectedIssue.getIssueID());
        jTextField2.setText(selectedIssue.getLineName());
        jTextField3.setText(selectedIssue.getDescription());
        jTextField4.setText(selectedIssue.getStatus());
        jTextField5.setText(selectedIssue.getRaisedBy());
    } else {
        JOptionPane.showMessageDialog(this, "No issue data available.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }

